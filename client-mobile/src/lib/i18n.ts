// Map browser language codes to supported languages
const detectBrowserLanguage = (): string => {
  if (typeof window === 'undefined') return 'de';
  
  try {
    // Get browser language (e.g., 'es-ES', 'en-US', 'de-DE')
    const browserLang = navigator.language || (navigator as any).userLanguage || 'de';
    const langCode = browserLang.split('-')[0].toLowerCase(); // Get 'es' from 'es-ES'
    
    // Map to supported languages
    const languageMap: Record<string, string> = {
      'de': 'de', // Deutsch
      'en': 'en', // English
      'es': 'es', // Español
      'nl': 'nl', // Nederlands
      'it': 'it', // Italiano
      'fr': 'fr', // Français
    };
    
    return languageMap[langCode] || 'de';
  } catch (error) {
    console.error('Error detecting browser language:', error);
    return 'de';
  }
};

// Load language from localStorage or use default
const getStoredLanguage = () => {
  try {
    // First, check if user has explicitly set a language
    const stored = localStorage.getItem('i18nextLng');
    if (stored && ['de', 'en', 'es', 'nl', 'it', 'fr'].includes(stored)) {
      return stored;
    }
    
    // If no stored language, detect from browser
    const detected = detectBrowserLanguage();
    // Save detected language for future use
    localStorage.setItem('i18nextLng', detected);
    return detected;
  } catch (error) {
    console.error('Error loading language from localStorage:', error);
    return detectBrowserLanguage();
  }
};

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: getStoredLanguage(),
    fallbackLng: 'de',
    interpolation: {
      escapeValue: false,
    },
  });

// Save language to localStorage when it changes
i18n.on('languageChanged', (lng) => {
  try {
    localStorage.setItem('i18nextLng', lng);
  } catch (error) {
    console.error('Error saving language to localStorage:', error);
  }
});

export default i18n;
